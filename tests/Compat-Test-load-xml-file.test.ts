// Ports the C# example from "How to load XML from a file".
// Source: https://learn.microsoft.com/en-us/dotnet/standard/linq/load-xml-file
//
// COMPAT: substituted — `XElement.Load("books.xml")` becomes
// `XElement.parse(...)` with the canonical Books sample inline. The point of
// the example is to demonstrate that the parsed tree round-trips through
// Console.WriteLine.

import { describe, it } from 'vitest';
import { XElement } from 'ltxmlts';
import { createConsole, expectMatches } from './Compat-Test-_helpers.js';

describe('Load XML document from a file (conceptual)', () => {
  it('XElement.parse on the canonical Books sample round-trips through Console.WriteLine', () => {
    const con = createConsole();

    const booksXml = `<Catalog>
  <Book id="bk101">
    <Author>Garghentini, Davide</Author>
    <Title>XML Developer's Guide</Title>
    <Genre>Computer</Genre>
    <Price>44.95</Price>
    <PublishDate>2000-10-01</PublishDate>
    <Description>An in-depth look at creating applications
      with XML.</Description>
  </Book>
  <Book id="bk102">
    <Author>Garcia, Debra</Author>
    <Title>Midnight Rain</Title>
    <Genre>Fantasy</Genre>
    <Price>5.95</Price>
    <PublishDate>2000-12-16</PublishDate>
    <Description>A former architect battles corporate zombies,
      an evil sorceress, and her own childhood to become queen
      of the world.</Description>
  </Book>
</Catalog>`;

    const booksFromFile = XElement.parse(booksXml);
    con.writeLine(booksFromFile);

    expectMatches(
      con.text(),
      `<Catalog>
  <Book id="bk101">
    <Author>Garghentini, Davide</Author>
    <Title>XML Developer's Guide</Title>
    <Genre>Computer</Genre>
    <Price>44.95</Price>
    <PublishDate>2000-10-01</PublishDate>
    <Description>An in-depth look at creating applications
      with XML.</Description>
  </Book>
  <Book id="bk102">
    <Author>Garcia, Debra</Author>
    <Title>Midnight Rain</Title>
    <Genre>Fantasy</Genre>
    <Price>5.95</Price>
    <PublishDate>2000-12-16</PublishDate>
    <Description>A former architect battles corporate zombies,
      an evil sorceress, and her own childhood to become queen
      of the world.</Description>
  </Book>
</Catalog>`,
    );
  });
});
